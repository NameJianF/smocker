package com.jenetics.smocker.util;

public class SmockerException extends Exception {

	public SmockerException() {
		super();
	}

	public SmockerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SmockerException(String message, Throwable cause) {
		super(message, cause);
	}

	public SmockerException(String message) {
		super(message);
	}

	public SmockerException(Throwable cause) {
		super(cause);
	}
	

}
